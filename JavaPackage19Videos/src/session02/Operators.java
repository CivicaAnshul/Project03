package session02;

public class Operators {

	public static void main(String[] args) {
		int a=10,	b=5;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(--b);
	}
}
